let currentIndex = 0;
const cardContainer = document.getElementById('card-container');
const prevButton = document.getElementById('prev');
const nextButton = document.getElementById('next');
function displayCards() {
    cardContainer.innerHTML = '';

    for (let i = currentIndex; i < currentIndex + 3 && i < cardsData.length; i++) {
        const card = document.createElement('div');
        card.className = 'col-lg-4';
        card.innerHTML = `
        <div class= 'card'>
            <img class="p-2 rounded-4 imgSizeCard" src="${cardsData[i].image}" class="card-img-top" alt="...">
  <div class="card-body ps-2 pb-0 d-flex">

     <img class="logoAppSizeCard" src="${cardsData[i].logoApp}" class="" alt="...">

    <div class="ms-3">
    <h6 class="card-title mb-0">${cardsData[i].titele}</h6>
    <p class="mb-0">${cardsData[i].p}</p>
    <p class=" text-muted">${cardsData[i].rating}</p>
   
       </div> `;
        cardContainer.appendChild(card);
    }

    // Show or hide the Prev button based on currentIndex
    prevButton.style.display = currentIndex > 0 ? 'flex' : 'none';
    nextButton.style.display = currentIndex + 3 < cardsData.length ? 'flex' : 'none';
}

function moveRight() {
    if (currentIndex + 3 < cardsData.length) {
        currentIndex++;
        displayCards();
    }
}

function moveLeft() {
    if (currentIndex > 0) {
        currentIndex--;
        displayCards();
    }
}

// Initial display
displayCards();